//concatenete(duas arrays e fazer uma so)
//slice (dividir uma em duas)

let numbers = [5, 6, 7, 8]
let letters = ['a', 'b', 'c']
// to make the concanenete: combine two arrays
all = numbers.concat(letters)//letters are inside numbers
console.log(all)
// to make the slice: take a piece of an array and create a new one.
half = all.slice(3, 5)//after 3 and before 5.
console.log(half)